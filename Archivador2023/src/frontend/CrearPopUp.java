    private void selectTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectTipoActionPerformed
        // TODO add your handling code here:
        Object selectedItem = selectTipo.getSelectedItem();

        // Verifica si el objeto seleccionado no es nulo
        if (selectedItem != null) {
            String selectedString = selectedItem.toString();
            
            boolean esDocumento = selectedString.equals("Documento");
            inputExtension.setEnabled(esDocumento);
            txtExtension.setEnabled(esDocumento);
            inputTamaño.setEnabled(esDocumento);
            txtTamaño1.setEnabled(esDocumento);
            
            if (esDocumento == false) {
                inputExtension.setText("");
                inputTamaño.setText("0");
            }
            
            if(esDocumento == true) {
                inputTamaño.setText("");
            }
        }
    }//GEN-LAST:event_selectTipoActionPerformed

