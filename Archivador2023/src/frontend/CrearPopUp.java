    private void btnCrear1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrear1ActionPerformed
        // TODO add your handling code here:
        FileType tipo = null;
        
        if(selectTipo.getSelectedItem().toString().equals("Documento")) {
            tipo = FileType.DOCUMENTO;
        }
        
        if(selectTipo.getSelectedItem().toString().equals("Carpeta")) {
            tipo = FileType.CARPETA;
        }
        
        try {
            Integer.parseInt(inputTamaño.getText());
        } catch (NumberFormatException excepcion) {
            JLabel mensaje = new JLabel("Ingrese un numero en el input de tamaño");
            mensaje.setFont(new Font("Arial", Font.BOLD, 24));
            JOptionPane.showMessageDialog(null, mensaje);
            return ;
        }
        
        // Notificar al listener (Principal)
        if (listener != null) {
            listener.onObjectCreated(inputNombre.getText(), tipo, Integer.parseInt(inputTamaño.getText()), inputExtension.getText());
        }

        this.dispose();
    }//GEN-LAST:event_btnCrear1ActionPerformed

